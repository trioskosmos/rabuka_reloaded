#[test]
fn test_flaw_gain_ability_not_fully_implemented() {
    // FLAW: gain_ability only tracks as prohibition effect, doesn't actually grant abilities
    // Ability: "ライブの合計スコアを＋1する。"を得る" (gain "live total score +1")
    // Parser generates: action: "gain_ability", ability: [...]
    // Engine: Only tracks as prohibition effect, doesn't grant the ability
    
    let mut game_state = GameState::new();
    let mut card_db = CardDatabase::new();
    
    let member_card = Card {
        card_no: "TEST-MEMBER-001".to_string(),
        name: "Test Member".to_string(),
        group: "Test".to_string(),
        unit: "Test".to_string(),
        cost: Some(1),
        blade_heart: None,
        blade: None,
        heart: vec![],
        score: None,
        required_hearts: vec![],
        card_type: "member".to_string(),
        text: "".to_string(),
        ..Default::default()
    };
    
    let member_card_id = card_db.add_card(member_card);
    
    let mut resolver = AbilityResolver::new(&mut game_state, &card_db);
    
    // Setup: Member on stage
    game_state.player1.stage.stage[1] = member_card_id;
    game_state.activating_card = Some(member_card_id);
    
    // Create gain_ability effect (as generated by parser)
    let effect = AbilityEffect {
        text: "ライブの合計スコアを＋1する。".to_string(),
        action: "gain_ability".to_string(),
        ability: Some(vec!["{{jyouji.png|常時}}ライブの合計スコアを＋1する。".to_string()]),
        duration: Some("live_end".to_string()),
        ..Default::default()
    };
    
    // Execute effect
    let result = resolver.execute_gain_ability(&effect);
    assert!(result.is_ok());
    
    // Check if ability was actually granted to the card
    // Currently, it's only tracked in prohibition_effects
    // The card's abilities list is not updated
    let card = card_db.get_card(member_card_id).unwrap();
    // This will likely show that the ability was not actually added to the card
    assert!(card.abilities.is_empty() || !card.abilities.iter().any(|a| a.text.contains("合計スコアを＋1")));
}

#[test]
fn test_flaw_user_choice_resolution_no_continuation() {
    // FLAW: No continuation mechanism for user choices in multi-step abilities
    // Ability: "手札を1枚控え室に置いてもよい：カードを2枚引く"
    // Parser generates: sequential with optional cost, then effect
    // Engine: After user choice for optional cost, execution stops instead of continuing
    
    let mut game_state = GameState::new();
    let mut card_db = CardDatabase::new();
    
    let hand_card = Card {
        card_no: "TEST-HAND-001".to_string(),
        name: "Hand Card".to_string(),
        group: "Test".to_string(),
        unit: "Test".to_string(),
        cost: Some(1),
        blade_heart: None,
        blade: None,
        heart: vec![],
        score: None,
        required_hearts: vec![],
        card_type: "member".to_string(),
        text: "".to_string(),
        ..Default::default()
    };
    
    let deck_card = Card {
        card_no: "TEST-DECK-001".to_string(),
        name: "Deck Card".to_string(),
        group: "Test".to_string(),
        unit: "Test".to_string(),
        cost: Some(1),
        blade_heart: None,
        blade: None,
        heart: vec![],
        score: None,
        required_hearts: vec![],
        card_type: "member".to_string(),
        text: "".to_string(),
        ..Default::default()
    };
    
    let hand_card_id = card_db.add_card(hand_card);
    let deck_card_id = card_db.add_card(deck_card);
    
    let mut resolver = AbilityResolver::new(&mut game_state, &card_db);
    
    // Setup: Card in hand, card in deck
    game_state.player1.hand.add_card(hand_card_id);
    game_state.player1.main_deck.cards.push(deck_card_id);
    
    // Create sequential effect with optional cost (as generated by parser)
    let effect = AbilityEffect {
        text: "手札を1枚控え室に置いてもよい：カードを2枚引く".to_string(),
        action: "sequential".to_string(),
        actions: Some(vec![
            AbilityEffect {
                text: "手札を1枚控え室に置いてもよい".to_string(),
                action: "move_cards".to_string(),
                source: Some("hand".to_string()),
                destination: Some("discard".to_string()),
                count: Some(1),
                optional: Some(true),
                ..Default::default()
            },
            AbilityEffect {
                text: "カードを2枚引く".to_string(),
                action: "draw_card".to_string(),
                count: Some(2),
                source: Some("deck".to_string()),
                destination: Some("hand".to_string()),
                ..Default::default()
            }
        ]),
        ..Default::default()
    };
    
    // Execute effect
    let result = resolver.execute_sequential(&effect);
    // This will set pending_choice for the optional cost
    // After user provides choice, the second action should execute
    // But the engine lacks a continuation mechanism to resume
    // This test documents the architectural flaw
    assert!(result.is_ok());
    // After providing choice result, the second action won't execute automatically
}

